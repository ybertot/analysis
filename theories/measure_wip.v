Lemma epsilon_trick (R : realType) (A : (\bar R)^nat) (e : R) (P : pred nat) :
  (0 <= e)%R -> (forall n, 0%:E <= A n) ->
  \sum_(i <oo | P i) (A i + (e / (2 ^ i)%:R)%:E) <=
  \sum_(i <oo | P i) A i + (2 * e)%R%:E.
